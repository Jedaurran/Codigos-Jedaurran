def areaCuadrado():
    lado= float(input("digite el valor del lado:"))
    print("el resultado de el area del cuadrado es", lado**2 )


def areaRectangulo():
    base= float(input("digite el valor de la base:"))
    altura= float(input("digite el valor de la altura:"))
    print("el resultado del area del rectangulo es", base*altura)

def areaCirculo():
    radio= float(input("digite el valor del radio:"))
    pie= 3.1416
    print("el resultado del area del circulo es", radio**2 *pie)
