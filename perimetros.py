
def perimetroCuadrado():
    lado= float(input("digite el valor de el lado:"))
    print("el resultado del perimetro del cuadrado es", lado*4)
perimetroCuadrado()
def perimetroRectangulo():
    base= float(input("digite el valor de la base:"))
    altura= float(input("digite el valor de la altura:"))
    print("el resultado del perimetro del rectangulo es", base*2 + altura*2)
perimetroRectangulo()
def perimetroCirculo():
    radio= float(input("digite el valor del radio:"))
    pie= 3.1416
    print("el perimetro del circulo es", radio * pie*2)
perimetroCirculo()
 
